"""
ABC085B

一旦全探索でやってみる

- 1000: N, 5000: 0, 10000: 0
- 1000: N - 1, 5000: 1, 10000: 0
...
- 1000: N - N, 5000: N, 10000: 0
- 1000: N - 1, 5000: 0, 10000: 1
- 1000: N - 1, 5000: 0, 10000: 1
- 1000: N - 2, 5000: 1, 10000: 1
- 1000: N - 2, 5000: 1, 10000: 1
- 1000: N - 2, 5000: 0, 10000: 2
...
- 1000: 0, 5000: 0, 10000: N
"""

N, Y = tuple([int(s) for s in input().split()])


def satsu_maisuu():
    for i_10000 in range(N + 1):
        for i_5000 in range(N - i_10000 + 1):
            i_1000 = N - (i_10000 + i_5000)
            if Y == (10000 * i_10000 + 5000 * i_5000 + 1000 * i_1000):
                return i_10000, i_5000, i_1000

    return -1, -1, -1


print("{} {} {}".format(*satsu_maisuu()))
